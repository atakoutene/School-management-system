package Model;

import java.io.Serializable;
import java.util.Objects;

/**
 *
 * @author Sokeng Paul (AG)
 */
public class SchoolLevel implements Serializable, Cloneable, Comparable<SchoolLevel>{
    private int id;
    private String name;
    
    public SchoolLevel(){
        
    }
    
    public SchoolLevel(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }

    @Override
    public int hashCode() {
        int hash = 3;
        hash = 73 * hash + this.id;
        hash = 73 * hash + Objects.hashCode(this.name);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final SchoolLevel other = (SchoolLevel) obj;
        if (this.id != other.id) {
            return false;
        }
        if (!Objects.equals(this.name, other.name)) {
            return false;
        }
        return true;
    }
    
    
    @Override
    public int compareTo(SchoolLevel o) {
        if(id < o.getId())
            return -1;
        else if (id > 1)
                return 1;
             else
                return 0;
    }
}
